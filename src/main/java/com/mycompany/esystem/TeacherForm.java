    private void showRecord() {
        DefaultTableModel tbModel = (DefaultTableModel) studTable.getModel();
        tbModel.setRowCount(0);

        ESystem repo = new ESystem();
        repo.DBConnect();

        try {
            String query = "SELECT * FROM teachers;";
            repo.resultSet = repo.statement.executeQuery(query);

            while (repo.resultSet.next()) {
                String teacherID = repo.resultSet.getString("teacherID");
                String name = repo.resultSet.getString("name");
                String address = repo.resultSet.getString("address");
                String contact = repo.resultSet.getString("contact");
                String department = repo.resultSet.getString("department");

                String[] items = {teacherID, name, address, contact, department};
                tbModel.addRow(items);
            }

            System.out.println("Load Success!");
        } catch (Exception ex) {
            System.out.println("Failed to load");
            System.out.println();
            System.out.println(ex);
        }
    }

